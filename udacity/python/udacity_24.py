# Verify the outout of array list
arr = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

print(arr[:3]) #-> Print from position 0 to 3 = a,b,c
print(arr[4:]) #-> Print from position 4 to the last = e,f,g
print(arr[4:6]) #-> Print from position 4 to 5 = e and f
print(arr[2:5]) #-> Print from position 2 to 4 = c,d,e
print(arr[3:7]) #-> Print from position 3 to 6 = d,e,f,g
print(arr[2:6]) #-> Print from position 2 to 5 = c,d,e,f
print(arr[:2]) #-> Print the two first elements = a,b
print(arr[0:2]) #-> Print from position 0 to 1 = a,b